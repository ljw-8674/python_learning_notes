"""
小明的成绩从去年的72分提升到了今年的85分，
请计算小明成绩提升的百分点，
并用字符串格式化显示出'xx.x%'，只保留小数点后2位：
"""
s1 = 72
s2 = 85
r = (s2 - s1) / s1
# 方法一
print(f'小明成绩同比提升了{(r*100):.2f}%')

# 方法二
print(f'小明成绩同比提升了{r:.2%}')

print('*' * 50)

x = 0.125
print(f'{x:.2%}')  # 12.50%
x = 1/4
print(f"{x:.1%}")  # 25.0%
x = 0.08
print(f"{x*100:.2f}%")  # 8.00%
print(f"{x:.2%}")  # 8.00%

